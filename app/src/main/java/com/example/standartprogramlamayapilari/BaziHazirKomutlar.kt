package com.example.standartprogramlamayapilari

import kotlin.math.*
import kotlin.random.Random

fun main (){

    val rastgelesayi = Random.nextInt(0,100) //Rastgele sayı üretir.
    println(rastgelesayi)

    val c = ceil(6.5)
    println(c)

    val f = floor(6.5)
    println(f)

    val s = sqrt(4.0)
    println(s)

    val a = abs(-10)
    println(a)

    val mx = max(100,200)
    println(mx)

    val mn = min(100,200)
    println(mn)

    val p = 2.0.pow(3)
    println(p)







}